
//-função quilogramaParaLibra:
export function quilogramaParaLibra(quilogramas) {
    return quilogramas * 2.20462;
}
//-função metroParaPe:
export function metroParaPe(metros) {
    return metros * 3.28084;
}
//-função celsiusParaFahrenheit:
export function celsiusParaFahrenheit(celsius) {
    return (celsius * 9 / 5) + 32;
}
